<script>
  function openAppointmentForm() {
    document.getElementById('appointmentForm').classList.remove('hidden');
    document.getElementById('overlay').classList.remove('hidden');
  }

  function closeAppointmentForm() {
    document.getElementById('appointmentForm').classList.add('hidden');
    document.getElementById('overlay').classList.add('hidden');
    document.getElementById('AppointmentForm').style.opacity = '1';
    document.getElementById('msgbox').innerHTML = '';

  }

  $(document).ready(() => {
    $('#AppointmentForm').on("submit", (e) => {
      e.preventDefault()
      // console.log(e.target)
      FormData = {
        name: $('#name').val(),
        age: $('#age').val(),
        phone: $('#phone').val(),
        email: $('#email').val(),
        prefDate: $('#prefDate').val(),
      }
      $.ajax({
        type: 'POST',
        data: FormData,
        url: 'partials/appointment-form.php',
        beforeSend: function() {
            $('#AppointmentForm').css("opacity", "0.3");
            $('#formLoading1').css("display", "block");
        },
        success: function (response) {
          $('#msgbox').html('Form Sent Successfully!')
          console.log(response)
          
          $('#formLoading1').css("display", "none");
          
          setTimeout(() => {
            $('#AppointmentForm').css("opacity", "1");
            document.getElementById('AppointmentForm').reset();
            closeAppointmentForm()
          }, 3000);
        }
      })
    })
    $('#AppointmentForm2').on("submit", (e) => {
      e.preventDefault()
